const { Command, CommandoMessage } = require("discord.js-commando");


module.exports = class jaccepte_pasCommand extends Command {
    constructor(client) {
        super(client, {
            name: 'jaccepte_pas',
            aliases: ['jacceptepas','jaccepte_pas','jaccp','jacc_p'],
            group: 'vocal',
            memberName: 'jaccepte_pas',
            description: "Ma meilleur phrase philosophique."
        });
    }

    /**
     * 
     * @param {CommandoMessage} message 
     * @param {String} arg 
     */
    async run(message, { arg }) {
        let cible = message.mentions.members.first();
        const audio = './src/Audio/jaccepte_pas.mp3'
        const volume = 0.5
      
        if (arg != null){
          if (cible) {//Si la cible existe que se soit l'autheur ou un autre membre
            message.channel.bulkDelete(1);
            const connection = await cible.voice.channel.join();
            const dispatcher = connection.play(audio);
      
            dispatcher.setVolume(volume);
      
            dispatcher.on('finish', () => {
               connection.disconnect();
            });
      
          } else {
            message.reply('Il existe pas ! Connard');
          }
      
        }else {
      
          const connection = await message.member.voice.channel.join();
          const dispatcher = connection.play(audio);
      
          dispatcher.setVolume(volume);
      
          dispatcher.on('finish', () => {
             connection.disconnect();
          });
        }
    }
}